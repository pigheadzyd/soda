#include "WATCard.h"					// Definition of WATCard calss


//--------------------------------------------------------------------------------------------------------------------
// Starup.
//--------------------------------------------------------------------------------------------------------------------
WATCard::WATCard(): balance( 0 ) {
}	// WATCard::WATCard


//--------------------------------------------------------------------------------------------------------------------
// Deposit the amount into WATCard.
//--------------------------------------------------------------------------------------------------------------------
void WATCard::deposit( unsigned int amount ) {
	balance += amount;
}	// WATCard::deposit


//--------------------------------------------------------------------------------------------------------------------
// Withdraw the amount from WATCard
//--------------------------------------------------------------------------------------------------------------------
void WATCard::withdraw( unsigned int amount ) {
	balance -= amount;
}	// WATCard::withdraw


//--------------------------------------------------------------------------------------------------------------------
// Get the balance from the WATCard.
//--------------------------------------------------------------------------------------------------------------------
unsigned int WATCard::getBalance() {
	return balance;
}	// WATCard::getBalance

